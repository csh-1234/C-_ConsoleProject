int playerX = 1;
int playerY = 1;

string buffer[MAP_HEIGHT] = {};
int buffer2[50][50] = {};

const int ViewSizeX = 20;
const int ViewSizeY = 10;

void MovePlayer()
{
    // (y,x)  up : (-1, +0), down (+1, +0), left(0,+1), right(0,-1)
    int newPosX = playerX;
    int newPosY = playerY;
    Manager& manager = Manager::getInstance();

    if (manager.input.GetKey(eKeyCode::Up)) { newPosY -= 1; }
    else if (manager.input.GetKey(eKeyCode::Down)) { newPosY += 1; }
    else if (manager.input.GetKey(eKeyCode::Left)) { newPosX -= 1; }
    else if (manager.input.GetKey(eKeyCode::Right)) { newPosX += 1; }

    if (map1[newPosY][newPosX] == (int)EGameObject::EMPTY)
    {
        playerX = newPosX;
        playerY = newPosY;
    }
}
void DrawBuffer()
{
    for (size_t y = 0; y < MAP_HEIGHT; y++)
    {
        buffer[y] = "";
        for (size_t x = 0; x < MAP_WIDTH; x++)
        {
            if (x == playerX && y == playerY)
            {
                buffer[y] += "★";
            }
            else if (map1[y][x] == 1)
            {
                buffer[y] += "■";
            }
            else if (map1[y][x] == 3)
            {
                buffer[y] += "♣";
            }
            else if (map1[y][x] == 4)
            {
                buffer[y] += "♠";
            }
            else
            {
                buffer[y] += "  ";
            }
        }
    }
}
void DrawBuffer2()
{
    for (size_t y = 0; y < MAP_HEIGHT; y++)
    {
        for (size_t x = 0; x < MAP_WIDTH; x++)
        {
            buffer2[y][x] = map1[y][x];
        }
    }
}
void renderBuffer2()
{
    int startX = playerX - (ViewSizeX / 2);
    int endX = playerX + (ViewSizeX / 2);
    int startY = playerY - (ViewSizeY / 2);
    int endY = playerY + (ViewSizeY / 2);

    if (startX < 0) startX = 0;
    if (startY < 0) startY = 0;

    // 0 ~ 5 
    // 0
    for (size_t y = 0; y < MAP_HEIGHT; y++)
    {
        for (size_t x = 0; x < MAP_WIDTH; x++)
        {
            // y가 starty보다 크거나 같고 endy 보다 작거나 같다
            if ((startY <= y && y <= endY) && (startX <= x && x <= endX))
            {
                if (x == playerX && y == playerY)
                {
                    cout << "★";
                }
                else if (map1[y][x] == 1)
                {
                    cout << "■";
                }
                else if (map1[y][x] == 3)
                {
                    cout << "♣";
                }
                else if (map1[y][x] == 4)
                {
                    cout << "♠";
                }
                else
                {
                    cout << "  ";
                }
            }
            else
            {
                cout << "  ";
            }
        }
        cout << endl;
    }
}

void renderBuffer()
{
    for (size_t y = 0; y < MAP_WIDTH; y++)
    {
        cout << buffer[y] << '\n';
    }
}
void CursorView()
{
    CONSOLE_CURSOR_INFO cursorInfo = { 0, };
    cursorInfo.dwSize = 1; //커서 굵기 (1 ~ 100)
    cursorInfo.bVisible = FALSE; //커서 Visible TRUE(보임) FALSE(숨김)
    SetConsoleCursorInfo(GetStdHandle(STD_OUTPUT_HANDLE), &cursorInfo);
}
void clearScreen()
{
    // 커서를 맨 위로 이동
    std::cout << "\033[H";
}

